<strong>Qu’est-ce qu’un cookie ?</strong>
<p>
  Un cookie est un petit fichier simple envoyé avec les pages de ce site web et stocké par votre navigateur sur le disque dur de votre ordinateur ou d’un autre appareil. Les informations qui y sont stockées peuvent être renvoyées à nos serveurs ou aux serveurs des tierces parties concernées lors d’une visite ultérieure. Pour ce faire les cookies communiquent des informations entre votre navigateur web (Firefox, Chrome, Internet Explorer, etc.) et le serveur qui héberge le site web, par exemple sur vos préférences d’affichage du site (langue, taille des caractères ...), votre historique de navigation, vos choix parmi les options proposées par un site ou encore des données d’enregistrement.
</p>

